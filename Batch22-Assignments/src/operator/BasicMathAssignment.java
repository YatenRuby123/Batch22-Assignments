package operator;

public class BasicMathAssignment {
	
	public static void main(String args[]){
		System.out.println("Integer Arithmatic");
		
		int a = 1 + 1;
		int b = a * 3;
		int c = b / 4;
		int d = c - a;
		int e = -d;
		
		System.out.println("Int a = " + a);
		System.out.println("Int b = " + b);
		System.out.println("Int c = " + c);
		System.out.println("Int d = " + d);
		System.out.println("Int e = " + e);

		System.out.println("\nfloating point Arithmatic");
		
		double da = 1 + 1;
		double db = da * 3;
		double dc = db / 4;
		double dd = dc - a;
		double de = -dd;
		
		System.out.println("Double da = " + da);
		System.out.println("Double db = " + db);
		System.out.println("Double dc = " + dc);
		System.out.println("Double dd = " + dd);
		System.out.println("Double de = " + de);
	
	
	}
	

}
